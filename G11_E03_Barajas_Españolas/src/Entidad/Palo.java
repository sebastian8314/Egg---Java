package Entidad;

/**
 *
 * @author sebas
 */
public class Palo {

    private String nombre;

    public Palo() {
    }

    public Palo(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }

}
